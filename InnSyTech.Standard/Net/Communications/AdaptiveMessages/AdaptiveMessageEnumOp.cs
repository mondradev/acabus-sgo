namespace InnSyTech.Standard.Net.Communications.AdaptiveMessages
{
    /// <summary>
    /// Define las operaciones válidas por los mensajes de enumeración.
    /// </summary>
    public enum AdaptiveMessageEnumOp
    {
        /// <summary>
        /// Operación para avanzar en la posición de la enumeración.
        /// </summary>
        NEXT,
        /// <summary>
        /// Operación para reiniciar la posición de la enumeración.
        /// </summary>
        RESET
    }
}
